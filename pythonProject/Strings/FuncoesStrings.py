
# Upper Case
string = 'python'
print(string.upper())

# Lower Case
print(string.lower())

# Dividir uma string por espaços em branco
string = 'Python é muito usado por cientistas de dados.'
print(string.split())
# Dividir uma string por um elemento específico
print(string.split('é'))

#capitalize
string = 'python'
print(string.capitalize())

#conta a quantidade de letras
print(string.count("p"))

#retorna a posição da letra
print(string.find('n'))

